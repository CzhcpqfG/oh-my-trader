# 卦象 -> 卦名 (通过上下卦组合)
def _make_hex_index() -> dict:
    """生成 (上卦, 下卦) -> 卦名 索引"""
    return {v[:2]: k for k, v in HEXAGRAMS.items()}


HEX_INDEX = _make_hex_index()


def get_hexagram_name(upper: str, lower: str) -> str:
    """通过上下卦名获取64卦名"""
    return HEX_INDEX.get((upper, lower), "")


